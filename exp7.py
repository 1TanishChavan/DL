import pandas as pd
import numpy as np
import tensorflow as tf
from tensorflow.keras.preprocessing.text import Tokenizer
from tensorflow.keras.preprocessing.sequence import pad_sequences
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Embedding, GRU, Dense, Dropout

# Load the dataset
data = pd.read_csv('/content/extended_conversations.csv')

# Prepare the input and output sequences
input_texts = data['Input'].values
target_texts = data['Response'].values

# Tokenize the input and output texts
tokenizer = Tokenizer()
tokenizer.fit_on_texts(input_texts + target_texts)
input_sequences = tokenizer.texts_to_sequences(input_texts)
target_sequences = tokenizer.texts_to_sequences(target_texts)

# Pad the sequences
max_sequence_length = max([len(seq) for seq in input_sequences + target_sequences])
input_sequences = pad_sequences(input_sequences, maxlen=max_sequence_length, padding='post')
target_sequences = pad_sequences(target_sequences, maxlen=max_sequence_length, padding='post')

# Create the target data for training
target_sequences = np.array(target_sequences)
target_sequences = np.expand_dims(target_sequences, -1)
# Define model parameters
vocab_size = len(tokenizer.word_index) + 1
embedding_dim = 100
gru_units = 256

# Build the model
model = Sequential()
model.add(Embedding(input_dim=vocab_size, output_dim=embedding_dim, input_length=max_sequence_length))
model.add(GRU(units=gru_units, return_sequences=True))
model.add(Dropout(0.2))
model.add(Dense(units=vocab_size, activation='softmax'))

# Compile the model
model.compile(optimizer='adam', loss='sparse_categorical_crossentropy', metrics=['accuracy'])
model.summary()
# Train the model
batch_size = 64
epochs = 200

model.fit(input_sequences, target_sequences, batch_size=batch_size, epochs=epochs, validation_split=0.2)
def generate_response(input_text):
    input_sequence = tokenizer.texts_to_sequences([input_text])
    input_sequence = pad_sequences(input_sequence, maxlen=max_sequence_length, padding='post')
    predicted_sequence = model.predict(input_sequence)
    predicted_text = tokenizer.sequences_to_texts(predicted_sequence.argmax(axis=-1))
    return predicted_text[0]

# Example usage
response = generate_response("Tell me a joke")
print(response)
